export function sum(num1, num2){
    return num1 + num2;
}

export function div(num1, num2){
    return num1 / num2;
}
export function sub(num1, num2){
    return num1 - num2;
}
export function mult(num1, num2){
    return num1 * num2;
}

export const mathOperations = {
    sum,
    sub,
    div,
    mult
}